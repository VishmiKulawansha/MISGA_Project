                        Bandaragama, Nuwara Eliya, Bandarawela, Lindula and also few areas in the Moneragala district.
                        areas. Check if we deliver to your area by using our Website.
                        You can type your address and see the list of stores available. 
                        We keep adding stores on a regular basis! If your favorite store is not listed 
                        you can suggest it to us via the &nbsp;“Request New Stores” button at the bottom of the
                        &nbsp; website.</p>

                        <p style="text-align:justify"><strong>3. How fast do you deliver?</strong></p>
                        <p style="text-align:justify">We deliver to you in the next hour, 
                        or at any other one-hour time-slot today and in the next 2 days.</p>

                        <p style="text-align:justify"><strong>4. What are the delivery hours?</strong></p>
                        <p style="text-align:justify">Our delivery hours are based on the opening hours of 
                        the stores we work with. Usually between 08am and 05pm. You can place your orders at 
                        any time of the day.</p>
                         
                        <p style="text-align:justify"><strong>5. What happens if any of my items are out of stock?</strong></p>
                        <p style="text-align:justify">We receive price and stock updates on a daily base from our retail partners. 
                        After placing your order, you will be asked what our Personal Shopper should do 
                        in case one of more of your items are out of stock. You can select for the whole 
                        order or for each unique product the following actions: choose “let shopper pick” 
                        if you want our shoppers to pick the most suitable replacement for you or “call me” 
                        if you prefer to receive a phone call. Alternatively, you can also select
                        “do not replace”.</p>

                        <p style="text-align:justify"><strong>6. Are there price differences from the store?</strong><br>
                        <p style="text-align:justify">For some stores, 
                        we may add a small service charge on top of the store price. 
                        If you notice an item that you think is priced incorrectly, please reach out to us!
                        We reserve the right to cancel items that are priced incorrectly due to error.</p>

                        <p style="text-align:justify"><strong>7. Would I get the shopping bag for my order?</strong></p>
                        <p style="text-align:justify">As long as the stocks are available, our Driver 
                        will put your order on our very own reuseable shopping bag. 
                        If you don’t get the shopping bag, it could be happened because the stocks are empty. 
                        But we will restock our shopping bag in the stores maximum at 1 week, don’t worry.</p>

                        <p style="text-align:justify"><strong>8.&nbsp;Can you assure the quality of the products that I bought?</strong></p>
                        <p style="text-align:justify">Of course! We can assure the quality of 
                        the items for you, our beloved customer. If you found any bad quality items, 
                        you can contact our Customer Service and we will replace it.</p>

                        <p style="text-align:justify"><strong>9. How do I edit or cancel my order?</strong></p>
                        <p style="text-align:justify">You can edit your order until the shopper starts picking your items. 
                        Go to “My Orders” section in the app and select the order you want to edit. 
                        Click on “Edit Order” to change one of the following:</p>
                        <ul>
                        <li>Add or remove items</li>
                        <li>Change delivery slot</li>
                        <li>Change payment method</li>
                        <li>Change address</li>
                        <li>Cancel order</li>
                        </ul>

                        <p style="text-align:justify"><strong>10. How do I report a problem with my order?</strong></p>
                        <p style="text-align:justify">You can contact us following the “Help” button in the 
                        app and selecting “Contact Us”. We respond very quickly!</p>
                        
                        <p style="text-align:justify"><strong>11. When will I receive my refund?</strong></p>
                        <p style="text-align:justify"> Please be kindly note, We will never refund the money 
                        anytime But we will the responsible for our products for only 2 days.If there is any damage or any uncomfortable things in oue products,
                        we will oromise to give you a new fresh products instead.  </p>

                        <p style="text-align:justify"><strong>12. How do I review my receipt?</strong></p>
                        <p style="text-align:justify">You will receive an electronic receipt via e-mail after your order is delivered.</p>

                        <p style="text-align:justify"><strong>13. How do I return items?</strong></p>
                        <p style="text-align:justify">If something seems to be wrong with your order, 
                        such as a missing item or an incorrect item, you can reject the items when our rider 
                        arrives at your doorstep. We will charge you only for the delivered items. 
                        Once an item is accepted by you, it cannot be returned. We do not currently 
                        support a return process for any items you may have accidentally purchased via website. 
                        You are more than welcome to coordinate returns directly with your store. 
                        Keep in mind that we do keep the in-store receipts for accounting purposes.</p>

                        <p style="text-align:justify"><strong>14. Why is my card authorized for more than my order total?</strong></p>
                        <p style="text-align:justify">Krish Villa temporarily authorizes your 
                        card for slightly more than your order total. As soon as your order is complete, we charge your 
                        card for exactly what was delivered. Your bank will update the final charge on your statement 
                        in 2 business days after delivery. The temporary authorization can be for up to Rs.1000 more 
                        than the order total. It helps account for charges like items sold by weight. If your final order 
                        total exceeds the authorization amount, there may be a second charge for the difference. 
                        You can see all charges reflected at the bottom of your receipt, which is available after delivery.</p>

                        <p style="text-align:justify"><strong>15. Who will select my items?</strong></p>
                        <p style="text-align:justify">All our products selection employees and Drivers are highly trained 
                        at selecting the best products for you.Our produce staff that pick products and 
                        Drivers to deliver to your doorstep.</p>

                        <p style="text-align:justify"><strong>16. Who will deliver my order?</strong></p>
                        <p style="text-align:justify">Your orders will be delivered by specially 
                        trained and screened Drivers.</p>

                        <p style="text-align:justify"><strong>17. What if all our produce staff are busy?</strong></p>
                        <p style="text-align:justify">Occasionally our produce staff get 
                        overwhelmed with orders. During these times, the next available delivery slot 
                        may be later in the day. Use the delivery checker in the app to see 
                        the next available delivery for your order.</p>

